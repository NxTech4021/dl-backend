import { Router } from 'express';
import { 
  getLeagueTypes, 
  getLeagueTypeById, 
  createLeagueType, 
  updateLeagueType, 
  deleteLeagueType 
} from '../controllers/leagueTypeController';

const router = Router();

router.get('/', getLeagueTypes);
router.get('/:id', getLeagueTypeById);
router.post('/', createLeagueType);
router.put('/:id', updateLeagueType);
router.delete('/:id', deleteLeagueType);

export default router;
